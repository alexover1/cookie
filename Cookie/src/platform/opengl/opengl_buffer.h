	class VertexBuffer
	{
	private:
		uint m_renderer_id;
	
	public:
		VertexBuffer(const void* data, uint size);
		~VertexBuffer();

		void bind() const;
		void unbind() const;
	};

	/////////////////////////////////////////////////////////////////////////////
	// IndexBuffer /////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	class IndexBuffer
	{
	private:
		uint m_renderer_id;
		uint m_count;

	public:
		IndexBuffer(const ushort* data, uint count);
		~IndexBuffer();

		void bind() const;
		void unbind() const;

		inline uint count() const { return m_count; }
	};

